/**
 * Devuelve `count` valores únicos de la propiedad `field` de estados
 * distintos al estado dado.
 */
window.getDistractors = function getDistractors(estados, estadoActual, field, count = 3) {
    const pool = estados
        .filter(e => e.id !== estadoActual.id)
        .map(e => {
            const val = e[field];
            return Array.isArray(val) ? val[0] : (typeof val === 'string' ? val.split('/')[0].trim() : val);
        })
        .filter((v, i, arr) => v && arr.indexOf(v) === i); // únicos y no vacíos

    const result = [];
    const copy = [...pool].sort(() => Math.random() - 0.5);
    for (const v of copy) {
        if (result.length >= count) break;
        result.push(v);
    }
    return result;
};

